export type EmployeeStatus = "active" | "leave" | "off-active";
export type EmploymentType = "full-time" | "intern" | "part-time" | "contract";
export type Priority = "high" | "medium" | "low";
export type TaskStatus = "todo" | "in-progress" | "done";
export type LeaveStatus = "Pending" | "Approved" | "Rejected";
export type LeaveType = "casual" | "sick" | "earned";
export type CandidateStage = "applied" | "screening" | "interview" | "offer" | "onboarded";

export interface Employee {
  id: string;
  name: string;
  role: string;
  department: string;
  status: EmployeeStatus;
  employmentType: EmploymentType;
  avatar: string;
  joinedAt: string;
  email: string;
  performance: number;
  tasksCompleted: number;
  lmsProgress: number;
  userId?: any;
}

export interface Task {
  id: string;
  title: string;
  assignedTo: string;
  priority: Priority;
  status: TaskStatus;
  dueDate: string;
  module: string;
  createdAt: string;
}

export interface Leave {
  id: string;
  employeeId: string;
  employeeName: string;
  type: LeaveType;
  fromDate: string;
  toDate: string;
  days: number;
  reason: string;
  status: LeaveStatus;
  appliedAt: string;
  approvedBy?: string;
}

export interface Candidate {
  id: string;
  name: string;
  role: string;
  stage: CandidateStage;
  aiMatchScore: number;
  appliedAt: string;
  resumeStrength: string;
  interviewScheduled: boolean;
}

export interface Course {
  id: string;
  title: string;
  category: string;
  duration: string;
  completedBy: number;
  totalEnrolled: number;
  instructor: string;
  modules: string[];
  color: string;
  progress?: number;
  xp?: number;
}

export interface Policy {
  id: string;
  title: string;
  category: string;
  lastUpdated: string;
  version: string;
  fileSize: string;
}

export interface Evaluation {
  id: string;
  employeeId: string;
  evaluator: string;
  date: string;
  rating: number;
  comment: string;
  category: string;
}

